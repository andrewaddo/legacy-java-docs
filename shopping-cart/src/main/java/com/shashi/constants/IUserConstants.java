package com.shashi.constants;

/**
 * Interface containing constants related to the user table and its columns in the database.
 */
public interface IUserConstants {

	/**
	 * Name of the user table in the database.
	 */
	public String TABLE_USER = "user";
	
	/**
	 * Column name for user's name.
	 */
	public String COLUMN_NAME = "name";
	
	/**
	 * Column name for user's mobile number.
	 */
	public String COLUMN_MOBILE = "mobile";
	
	/**
	 * Column name for user's email address.
	 */
	public String COLUMN_EMAIL = "email";
	
	/**
	 * Column name for user's address.
	 */
	public String COLUMN_ADDRESS = "address";
	
	/**
	 * Column name for user's pincode.
	 */
	public String COLUMN_PINCODE = "pincode";
	
	/**
	 * Column name for user's password.
	 */
	public String COLUMN_PASSWORD = "password";
}
